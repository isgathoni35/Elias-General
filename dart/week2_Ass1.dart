void main() {
  // Integer (int) to store a whole number
  int age = 25;
  print("Age: $age");

  // Double (double) to store a decimal number
  double pi = 3.14159;
  print("Pi: $pi");

  // String (String) to store text
  String name = "John Doe";
  print("Name: $name");

  // List (List) to store a collection of items
  List<String> colors = ["red", "green", "blue"];
  print("Colors: $colors");

  // Map (Map) to store key-value pairs
  Map<String, int> studentGrades = {"Math": 90, "English": 85};
  print("Student Grades: $studentGrades");
}
